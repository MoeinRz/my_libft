#include "libft.h"

//===========================================================================//
//Absolute Value                                                             //
//===========================================================================//
int	ft_abs(int n)
{
	if (n < 0)
		return (-n);
	return (n);
}

//===========================================================================//
//Get the Digit Length                                                       //
//===========================================================================//
int	ft_len(int n)
{
	int	len;

	len = 0;
	if (n <= 0)
		++len;
	while (n != 0)
	{
		++len;
		n = n / 10;
	}
	return (len);
}

//===========================================================================//
//Integer to Ascii Function                                                  //
//===========================================================================//
char	*ft_itoa(int n)
{
	char	*result;
	int		len;

	len = ft_len(n);
	result = malloc(sizeof(char) * (len + 1));
	if (!result)
		return (NULL);
	result[len] = '\0';
	if (n == 0)
		result[0] = '0';
	else if (n < 0)
		result[0] = '-';
	while (n != 0)
	{
		--len;
		result[len] = ft_abs(n % 10) + '0';
		n = n / 10;
	}
	return (result);
}
