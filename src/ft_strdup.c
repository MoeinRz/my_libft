#include "libft.h"

char	*ft_strdup(const char *str)
{
	char	*cpy;
	size_t	i;
	int		len;

	len = ft_strlen(str);
	cpy = malloc(sizeof(char) * len + 1);
	if (!cpy)
		return (NULL);
	i = 0;
	while (str[i])
	{
		cpy[i] = str[i];
		i++;
	}
	cpy[i] = '\0';
	return (cpy);
}
